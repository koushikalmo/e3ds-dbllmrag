# ============================================================
# lib/__init__.py — Package Marker
# ============================================================
# This empty file tells Python that the `lib/` folder is a
# package, meaning you can do:
#
#     from lib.mongodb import get_stream_db
#
# Without it, Python would not know that `lib/` is importable.
# It stays empty intentionally — all real code lives in the
# individual modules (mongodb.py, schemas.py, etc.).
# ============================================================
